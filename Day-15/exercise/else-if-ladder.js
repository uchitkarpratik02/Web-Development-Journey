let marks = "91";

if(marks >= 90){
    console.log("Grade: A+")
}
else if(marks >= 80){
    console.log("Grade: A")
}
else if(marks >= 70){
    console.log("Grade: B")
}
else if(marks >= 60){
    console.log("Grade: C")
}
else if(marks >= 45){
    console.log("Pass")
}
else if(marks < 45){
    console.log("Better Luck next Time")
}
else{console.log("Invalid")}

